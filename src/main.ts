import { NestFactory } from '@nestjs/core'
import { AppModule } from './app.module'
import { DocumentBuilder, SwaggerModule } from '@nestjs/swagger'
import { ValidationPipe } from '@nestjs/common'

async function bootstrap() {
  const app = await NestFactory.create(AppModule)

  // âœ… Prefixo global para todas as rotas
  app.setGlobalPrefix('api')

  // âœ… Habilita CORS para ambientes autorizados
  app.enableCors({
    origin: [
      'http://localhost:3000',
      'http://localhost:3001',
      'https://insurance-frontend-olive.vercel.app',
    ],
    credentials: true,
  })

  // âœ… Pipe global para validaÃ§Ãµes dos DTOs
  app.useGlobalPipes(
    new ValidationPipe({
      whitelist: true,
      forbidNonWhitelisted: true,
      transform: true,
    }),
  )

  // âœ… Swagger config
  const config = new DocumentBuilder()
    .setTitle('Insurance API')
    .setDescription('DocumentaÃ§Ã£o da API de Seguros')
    .setVersion('1.0')
    .addBearerAuth()
    .build()

  const document = SwaggerModule.createDocument(app, config)
  SwaggerModule.setup('api/docs', app, document)

  // âœ… Inicializa aplicaÃ§Ã£o
  await app.listen(3000)

  console.log(`ðŸš€ API rodando em http://localhost:3000/api`)
  console.log(`ðŸ“„ Swagger disponÃ­vel em http://localhost:3000/api/docs`)
}

bootstrap()
